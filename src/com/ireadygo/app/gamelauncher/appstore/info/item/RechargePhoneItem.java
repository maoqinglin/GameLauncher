package com.ireadygo.app.gamelauncher.appstore.info.item;

public class RechargePhoneItem {

	private long COrderId;
	private int ICurrency;
	private int IRabbitTicketMoney;
	private int IDiffCurrency;

	public long getCOrderId() {
		return COrderId;
	}

	public void setCOrderId(long cOrderId) {
		COrderId = cOrderId;
	}

	public int getICurrency() {
		return ICurrency;
	}

	public void setICurrency(int iCurrency) {
		ICurrency = iCurrency;
	}

	public int getIRabbitTicketMoney() {
		return IRabbitTicketMoney;
	}

	public void setIRabbitTicketMoney(int iRabbitTicketMoney) {
		IRabbitTicketMoney = iRabbitTicketMoney;
	}

	public int getIDiffCurrency() {
		return IDiffCurrency;
	}

	public void setIDiffCurrency(int iDiffCurrency) {
		IDiffCurrency = iDiffCurrency;
	}

}
